#!/bin/bash -xeu

./configure --prefix=/usr        \
            --bindir=/usr/bin    \
            --localstatedir=/var \
            --disable-logger     \
            --disable-whois      \
            --disable-rcp        \
            --disable-rexec      \
            --disable-rlogin     \
            --disable-rsh        \
            --disable-servers

make
# make check
make DESTDIR=$LFS install
mv -v $LFS/usr/{,s}bin/ifconfig
